# Create different types of tuples in Python

# Empty tuple
empty_tuple = ()
print("Empty tuple:", empty_tuple)

# Tuple with a single value
single_value_tuple = (42,)
print("Tuple with a single value:", single_value_tuple)

# Tuple with multiple values
multiple_values_tuple = (1, 2, 3, 4, 5)
print("Tuple with multiple values:", multiple_values_tuple)

# Tuple with collections (list and set)
collections_tuple = ([1, 2, 3], {4, 5, 6})
print("Tuple with collections:", collections_tuple)

# Tuple with different data types
mixed_tuple = (1, "string", 3.14, True, [5, 6], {"key": "value"})
print("Tuple with different data types:", mixed_tuple)
